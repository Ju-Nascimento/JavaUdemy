public class ValorPadrao {

    static int inteiro;
    static double varDouble;
    static char caractere;
    static boolean boleano;
    public static void main(String[] args) {
        /*Crie um programa Java que demonstre o valor padrão de variáveis primitivas (int,
        double, char, boolean) quando não inicializadas explicitamente. */
        System.out.println(varDouble);
        System.out.println(inteiro);
        System.out.println(caractere);
        System.out.println(boleano);

    }
}
